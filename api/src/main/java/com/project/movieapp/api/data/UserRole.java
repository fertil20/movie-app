package com.project.movieapp.api.data;


public class UserRole extends Identified<UserRole> {

    public UserRole(Id<UserRole> id, String name) {
        super(id);
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    private String name;
}
